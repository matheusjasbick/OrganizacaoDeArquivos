void CSVParser_init(CSVParser *parser)
{
    parser->state = START;
    for (int i = 0; i < CSV_MAX_ROW_SIZE; i++) {
        parser->buffer[i] = '\0';
    }
    parser->buffer_length = 0;
    parser->fields[0] = parser->buffer;
    parser->field_count = 0;
}

void CSVParser_processLines(CSVParser *parser, const char *input, int input_size,
                            void (*callback)(char **, int, void *), void *user_data)
{
    int state = parser->state;
    char c;
    while (state && (input_size--) && (c = *input++)) {
        int next_state = transition[state][(unsigned char)c];
        if ((state == QUOTED_STR && next_state != SEP_OR_NEWLINE) ||
            (state == QUOTED_STR_SPECIAL) || next_state == STR) {
            /* Character belongs to the current field: keep it.
             * Guard against a row longer than CSV_MAX_ROW_SIZE: drop the excess
             * instead of overrunning the buffer. Leaves room for the trailing
             * '\0' written when the field ends, so that write is always safe. */
            if (parser->buffer_length < CSV_MAX_ROW_SIZE - 1) {
                parser->buffer[parser->buffer_length++] = c;
            }
        } else if (next_state == ADD_FIELD || next_state == ADD_FIELD_AND_RECORD) {
            /* End of a field (and possibly the whole row). */
            parser->buffer[parser->buffer_length++] = '\0';
            parser->field_count++;
            parser->fields[parser->field_count] = parser->buffer + parser->buffer_length;
            if (next_state == ADD_FIELD_AND_RECORD) {
                parser->buffer_length = 0;
                if (parser->field_count) {
                    callback(parser->fields, parser->field_count, user_data);
                }
                parser->field_count = 0;
            }
            next_state = START;
        }
        state = next_state;
    }
    parser->state = state;
}
